package system;
public class Docs {

	private String D_;
	private String T_;
	private int C_;
	private String K_;

	public Docs(String D_, String T_, int C_, String K_) {
		this.D_ = D_;
		this.T_ = T_;
		this.C_ = C_;
		this.K_ = K_;
	}

	public String getD_() {
		return D_;
	}

	public void setD_(String d_) {
		D_ = d_;
	}

	public String getT_() {
		return T_;
	}

	public void setT_(String t_) {
		T_ = t_;
	}

	public int getC_() {
		return C_;
	}

	public void setC_(int c_) {
		C_ = c_;
	}

	public String getK_() {
		return K_;
	}

	public void setK_(String k_) {
		K_ = k_;
	}

}